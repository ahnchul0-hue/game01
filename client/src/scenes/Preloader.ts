// ============================================================
// Preloader.ts — 에셋 로딩 Scene
// ============================================================
// 게임에 필요한 모든 에셋을 로드하고 진행률을 표시한다.
// 로딩 완료 후 MainMenu로 전환한다.
//
// [preload — 로드할 에셋 목록]
//
// 1. 플레이어 스프라이트
//    - capybara-idle: 대기 프레임 (스프라이트시트 또는 단일 이미지)
//    - capybara-run: 달리기 애니메이션 프레임들
//    - capybara-jump: 점프 프레임
//    - capybara-slide: 슬라이드 프레임
//    → 프로토타입: 단일 색상 사각형 이미지로 대체
//
// 2. 배경 이미지 (패럴랙스용)
//    - bg-sky: 먼 배경 (하늘/구름) — TileSprite용 타일 이미지
//    - bg-trees: 중간 배경 (나무/산)
//    - bg-ground: 땅/레인 바닥
//    → 프로토타입: 단색 그라디언트 이미지로 대체
//
// 3. UI 이미지
//    - logo: 타이틀 로고
//    - btn-start: 시작 버튼
//    - btn-restart: 재시작 버튼
//    → 프로토타입: 텍스트로 대체 가능
//
// [create — 로딩 UI]
//
// 1. 화면 중앙에 로딩 바 표시
//    - 배경 바: 회색 사각형 (예: 400x30)
//    - 채움 바: 초록색 사각형, 진행률에 따라 width 증가
//
// 2. 진행률 이벤트 리스닝
//    - this.load.on('progress', (value: number) => { ... })
//    - value: 0.0 ~ 1.0 → 채움 바의 scaleX로 반영
//
// 3. 로딩 완료 이벤트
//    - this.load.on('complete', () => { ... })
//    - 모든 에셋 로드 완료 → MainMenu Scene으로 전환
//
// [주의사항]
// - 프로토타입 단계에서는 에셋이 거의 없으므로 로딩이 순식간
// - 최소 0.5초는 로딩 화면을 보여주는 것이 UX상 좋음
//   → delayedCall(500)로 최소 대기 시간 설정
// - 에셋 로드 실패 시 기본 도형 폴백 고려
// ============================================================
