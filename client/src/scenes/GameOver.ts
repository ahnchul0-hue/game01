// ============================================================
// GameOver.ts — 게임 오버 Scene
// ============================================================
// 게임 종료 후 결과를 표시하고, 재시작/메뉴 복귀를 제공한다.
//
// [Scene 데이터 수신]
// - init(data): Game Scene에서 전달받은 데이터
//   - data.score: 최종 점수
//   - data.distance: 최종 이동 거리
//   - data.items: 수집한 아이템 수 (M2에서 추가)
//
// [화면 레이아웃]
//
// ┌─────────────────────────┐
// │                         │
// │      게임 오버!          │  ← 상단: 타이틀
// │                         │
// │      🦫 (슬픈 표정)      │  ← 카피바라 이미지
// │                         │
// │   ⭐ 점수: 1,280        │  ← 최종 점수
// │   📏 거리: 523m         │  ← 최종 거리
// │   🍊 귤: 42개           │  ← 수집 아이템 (M2)
// │                         │
// │   [ 🔄 다시하기 ]        │  ← 재시작 버튼
// │   [ 🏠 메뉴로 ]          │  ← 메인메뉴 버튼
// │                         │
// └─────────────────────────┘
//
// [create]
//
// 1. 반투명 어두운 배경 오버레이
//    - Graphics로 검은 사각형, alpha 0.7
//
// 2. "게임 오버!" 텍스트
//    - 큰 폰트, 화면 상단 1/4
//    - 등장 Tween: 위에서 떨어지며 바운스
//
// 3. 결과 표시
//    - data에서 받은 score, distance 표시
//    - 숫자가 0에서 최종값까지 카운트업 애니메이션
//      → Tween으로 0 → 최종값 (duration 1초)
//
// 4. 재시작 버튼
//    - setInteractive() + 'pointerdown'
//    - 클릭 시: this.scene.start('Game', { mode: this.lastMode })
//    - 같은 모드로 재시작
//
// 5. 메뉴 버튼
//    - 클릭 시: this.scene.start('MainMenu')
//
// 6. 최고점수 체크 (M2에서 구현)
//    - localStorage에서 최고점수 비교
//    - 갱신 시 "NEW BEST!" 텍스트 표시
//
// [주의사항]
// - 재시작 시 Game Scene의 모든 상태가 초기화되어야 함
//   → Game.ts의 create()에서 초기화하므로 자동 처리
// - 점수 저장 API 호출은 M2에서 추가
// - 리더보드 표시는 M5에서 추가
